# Statement
# A grocery shop has
# ["Apple", "Banana", "Orange", "Mango"]
# Check whether
# Banana
# Grapes
# exist in the list.
# Print suitable messages.

fruit_list = ["Apple", "Banana", "Orange", "Mango"]

if "Banana" in fruit_list:
    print("Hurrah! Banana is available")
else :
    print("Alas! Banana is not available")

if "Grapes" in fruit_list :
    print("Hurrah! Grapes are available")
else : 
    print("Alas! Grapes are not available")