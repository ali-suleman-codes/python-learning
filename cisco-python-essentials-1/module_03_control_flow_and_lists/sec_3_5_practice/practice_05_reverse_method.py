# Use the reverse() method to display them in reverse order.

eras = ["Stone Age",
 "Bronze Age",
 "Iron Age",
 "Middle Ages",
 "Modern Age"]

print("Original order :", eras)

eras.reverse()
print("Reverse order  :", eras)