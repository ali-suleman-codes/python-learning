# Problem Statement

# A company is updating an employee's account balance.

# Requirements
# Create a variable named balance.
# Demonstrate all shortcut operators:
# +=
# -=
# *=
# /=
# //=
# %=
# **=
# Print the value after every operation.

balance = 100.0
print("Initial balance :", balance)

balance += 50
print("After += 50 :", balance)

balance -= 30
print("After -= 30 :", balance)

balance *= 2
print("After *2 :", balance)

balance /= 4
print("After /=4 :", balance)

balance //= 3
print("After //=3 :", balance)

balance %= 7
print("After %= 7 :", balance)

balance **= 2
print("After **= 2 :", balance)