# Problem Statement

# A scientist is calculating different formulas using fixed values.

# Requirements

# Create variables such as:

a = 8
b = 3
c = 2

# Calculate different mathematical expressions using these variables.

print("a + b * c   =", a + b * c)
print("a // b ** c =", a // b ** c)
print("a ** b ** c =", a ** b ** c)
print("a * (b + c) =", a * (b + c))
print("a / b - c   =", a / b - c)