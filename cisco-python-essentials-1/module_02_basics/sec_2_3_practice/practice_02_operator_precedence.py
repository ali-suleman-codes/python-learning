# Problem Statement

# Create a program that demonstrates how operator precedence affects mathematical expressions.

# Requirements

# Calculate and display the results of expressions such as:

# 5 + 2 * 3
# (5 + 2) * 3
# 20 / 5 + 2
# 20 / (5 + 2)
# 2 ** 3 * 2

# Display each expression and its result.

print("5 + 2 * 3    =", 5 + 2 * 3)
print("(5 + 2) * 3  =", (5 + 2) * 3)
print("20 / 5 + 2   =", 20 / 5 + 2)
print("20 / (5 + 2) =", 20 / (5 + 2))
print("2 ** 3 * 2   =", 2 ** 3 * 2)